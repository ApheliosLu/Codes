#include <iostream>		//Cpp的头文件
using namespace std;

/*
	C++头文件、标准输出函数cout、定义并输出变量
*/

int main()
{
	cout << "hello C++" << endl;	//C++标准输出 cout end of line;	cout << "一个字符串" << endl;

	//变量创建的语法：数据类型 变量名 = 初始值;
	int a = 10;		//创建变量a
	cout << "a =" << a << endl;	//输出a = 10

	system("pause");

	return 0;
}
